# OPERANDS:
# + | - | * | / | % (addition, subtraction, multplication, division, remainder)
# > | < (greater than, less than)
# == | != (equal to, not equal to)
# >= | <= (greater than or equal to, less than or equal to)

x = 10
y = 12

print(x == y) #False
print(x<=y) #True
print(x * y) #120
print(y % x) #2 (12 into 10 leaves 2 remaining)
print(x % y) #10 ()

